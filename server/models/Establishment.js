const mongoose = require("mongoose");

const { Schema } = mongoose;

const establishmentSchema = new Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  address: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  zipCode: {
    type: String,
    required: true,
  },
  phoneNumber: {
    type: String,
    required: true,
  },
});

const Establishment = mongoose.model("Establishment", establishmentSchema);

module.exports = Establishment;
